-- To execute the stored procedure, use the following format:
-- EXEC SP_SIMULATEBOARDINGS 'START_DATE', 'END_DATE', VEHICLE_ID, RECORD_COUNT
-- If VEHICLE_ID is NULL, transactions will be created for all vehicles.

CREATE PROCEDURE SP_SIMULATEBOARDINGS
@START_DATE DATETIME,
@END_DATE DATETIME,
@VEHICLE_ID INT = NULL, -- NULL OLDU–U ZAMAN T‹M ARA«LARDAN B›R›
@RECORD_COUNT INT = 2 -- OLUřTURULACAK KAYIT SAYISI
AS
BEGIN --MAIN
    SET NOCOUNT ON;
    
    -- DE–›řKENLER
    DECLARE @COUNTER INT = 1 -- SAYA«
    DECLARE @SUCCESS_COUNT INT = 0 -- BAřARILI KAYIT SAYISI
    DECLARE @RANDOM_DATE_TIME DATETIME --RANDOM  TAR›H 
    DECLARE @RANDOM_CARD_ID INT -- RANDOM KART ID 
    DECLARE @CARD_NO INT -- KART NO
    DECLARE @CARD_TYPE_ID INT --KART T›P› ID
    DECLARE @CURRENT_BALANCE DECIMAL(10,2) -- ř›MD›K› BAK›YE
    DECLARE @FARE_PRICE DECIMAL(10,2) -- ‹CRET F›YAT
    DECLARE @SELECTED_VEHICLE_ID INT; -- SE«›LEN ARA« ID
    DECLARE @AVAILABLE_CARDS_COUNT INT --UYGUN KART SAYISI
    DECLARE @AVAILABLE_VEHICLES_COUNT INT --UYGUN ARA« SAYISI
    
	-- PARAMETRE KONTROLLER›
	-- DATE KONTROLLER›
    IF @START_DATE >= @END_DATE
    BEGIN --1
		PRINT 'BAřLANGI« TAR›H› B›T›ř TAR›H›NDEN K‹«‹K OLMALI'
        RETURN
    END --1
    
	-- E–ER KAYIT SAYISI K‹«‹KSE
    IF @RECORD_COUNT <= 0
    BEGIN --2
		PRINT 'KAYIT SAYISI 0''DAN B‹Y‹K OLMALI'
        RETURN
    END --2
    
    -- KULLANILAB›L›R KART SAYISI KONTROL‹
    SELECT @AVAILABLE_CARDS_COUNT = COUNT(*) FROM CARDS -- SATIR SAYISINI D÷ND‹R
    WHERE ISACTIVE = 1 AND BALANCE > 0; -- KART AKT›F OLMALI VE BAK›YES› 0DEN B‹Y‹K OLMALI
    
	-- E–ER KULLANILAB›L›R KART YOKSA
    IF @AVAILABLE_CARDS_COUNT = 0
    BEGIN --3
        PRINT 'KULLANILAB›L›R KART YOK'
        RETURN
    END --3
    

    -- ARA« KONTROL‹
    IF @VEHICLE_ID IS NOT NULL --E–ER ARA« ID NULL DE–›LSE
    BEGIN --4
		
        IF NOT EXISTS (SELECT 1 FROM VEHICLE WHERE VEHICLE_ID = @VEHICLE_ID AND ISACTIVE = 1)
        BEGIN --4.1
			PRINT 'BEL›RT›LEN ARA« BULUNAMADI VEYA AKT›F DE–›L'
            RETURN
        END --4.1
		-- KUULANILAB›L›R ARA« SAYISINI 1E Eř›TLE
        SET @AVAILABLE_VEHICLES_COUNT = 1;
    END --4
    ELSE
    BEGIN --5
		-- KULLANILAB›L›R ARA« SAYISINI SA VE AKT›F› TRUE OLAN ARACI SE«
        SELECT @AVAILABLE_VEHICLES_COUNT = COUNT(*) FROM VEHICLE WHERE ISACTIVE = 1
        
		-- E–ER KULLANILAB›L›R ARA« SAYISI 0'A Eřř›TSE
        IF @AVAILABLE_VEHICLES_COUNT = 0
        BEGIN --5.1 
			PRINT 'AKT›F ARA« BULUNAMADI'
            RETURN
        END --5.1
    END --5
  

    WHILE @COUNTER <= @RECORD_COUNT AND @SUCCESS_COUNT < @RECORD_COUNT
    BEGIN --6
		-- BAřLANGI« TAR›H› VE B›T›ř TAR›H› ARASINDA RANDOM TAR›H SE«
        SET @RANDOM_DATE_TIME = DATEADD(SECOND, CAST(RAND() * DATEDIFF(SECOND, @START_DATE, @END_DATE) AS INT), @START_DATE)


        -- ARA« SE«›M›
        IF @VEHICLE_ID IS NULL
        BEGIN --6.1
			-- ARA«LAR TABLOSUNDAN AK›TF OLAN ARA«LARDAN RASTEGELE ARA« SE«T›K
            SELECT TOP 1 @SELECTED_VEHICLE_ID = VEHICLE_ID FROM VEHICLE WHERE ISACTIVE= 1 ORDER BY NEWID();
        END --6.1
        ELSE
        BEGIN --6.2
			-- SE«›LEN ARA« ID ›LE ARA« ID Eř›TLED›K
            SET @SELECTED_VEHICLE_ID = @VEHICLE_ID;
        END --6.2
        

        -- YETERL› BAK›YES› OLAN AKT›F OLAN KARTLARDAN NEWID ›LE SE«T›K
        SELECT TOP 1 
            @RANDOM_CARD_ID = CARD_ID,
            @CARD_NO = CARD_NO,
            @CARD_TYPE_ID = CARD_TYPE_ID,
            @CURRENT_BALANCE = BALANCE
        FROM CARDS WHERE ISACTIVE = 1 AND BALANCE > 0 ORDER BY NEWID()
        

        IF @RANDOM_CARD_ID IS NOT NULL
        BEGIN --6.3
            -- KART T›P›NE G÷RE ‹CRET AL
            SELECT @FARE_PRICE = PRICE FROM CARDTYPES WHERE CARD_TYPE_ID = @CARD_TYPE_ID AND ISACTIVE = 1
            
			-- BAK›YE KONTROL‹
            IF @CURRENT_BALANCE >= @FARE_PRICE -- EE–ER MEVCUT BAK›YE, ‹CRETTEN FAZLA ›SE
            BEGIN --6.3.1
                BEGIN TRY -- 6.3.1.A
                    -- TRANSACTION BAřLAT
                    BEGIN TRANSACTION;
                    
                    -- TRANSA. KAYDI OLUřTUR
                    INSERT INTO TRANSACTIONS(VEHICLE_ID, CARD_ID, CARD_NO, CARD_TYPE_ID, TRANSACTION_DATE, SPENT_AMOUNT, REMAINING_BALANCE)
                    VALUES(@SELECTED_VEHICLE_ID, @RANDOM_CARD_ID, @CARD_NO, @CARD_TYPE_ID, @RANDOM_DATE_TIME, @FARE_PRICE, @CURRENT_BALANCE - @FARE_PRICE);
                    
                    -- KART BAK›YES›N› G‹NCELLE
                    UPDATE CARDS
                    SET BALANCE = BALANCE - @FARE_PRICE, MODIFIED_DATE = GETDATE() WHERE CARD_ID = @RANDOM_CARD_ID;
					-- KART IDS›, RANDOM KART ID'S›NE Eř›T OLAN KARTLARIN BAK›YES›N›, BAK›YEDE KALAN SAYI ›LE G‹NCELLE, MODIFIED DATE ›«›N GETDATE AL .
                    
                    -- TRANSACTION ONAYLA
                    COMMIT TRANSACTION;
                    
					-- BřARILI KAYIT SAYISINI 1 ARTTIR
                    SET @SUCCESS_COUNT = @SUCCESS_COUNT + 1;

                END TRY --6.3.1.A
                BEGIN CATCH --6.3.1.B
                    -- HATA OLURSA ›řLEM› GER› AL
                    IF @@TRANCOUNT > 0
                        ROLLBACK TRANSACTION;
                   
                END CATCH --6.3.1.B
			END --6.3.1
        END --6.3
        
        -- DE–›řKENLER› SIFIRLA
        SET @RANDOM_CARD_ID = NULL;
        SET @CARD_NO = NULL;
        SET @COUNTER = @COUNTER + 1;
      
    END --6
    
    -- OLUřTURULAN TRANSACTIONLARI G÷STER
    IF @SUCCESS_COUNT > 0
    BEGIN --7
        SELECT TOP (@SUCCESS_COUNT)
            T.TRANSACTION_ID,
            V.PLATE,
			T.CARD_ID,
            T.CARD_NO,
            CT.CARD_TYPE,
            T.TRANSACTION_DATE,
            T.SPENT_AMOUNT,
            T.REMAINING_BALANCE
        FROM TRANSACTIONS T
        INNER JOIN VEHICLE V ON T.VEHICLE_ID = V.VEHICLE_ID
        INNER JOIN CARDTYPES CT ON T.CARD_TYPE_ID = CT.CARD_TYPE_ID
        ORDER BY T.TRANSACTION_ID DESC;
    END --7

	SELECT * FROM TRANSACTIONS
END -- MAIN


-- DENEME KISMI
 EXEC SP_SIMULATEBOARDINGS 
     @START_DATE = '2025-01-01',
     @END_DATE = '2025-01-10', 
     @VEHICLE_ID = NULL,  
     @RECORD_COUNT = 2;
