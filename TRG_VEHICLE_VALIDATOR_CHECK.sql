CREATE TRIGGER TRG_VEHICLE_VALIDATOR_CHECK
ON VEHICLE
AFTER INSERT, UPDATE
AS
BEGIN --1

	SET NOCOUNT ON

	-- DE–›řKENLER
	DECLARE @VALIDATOR_NO INT
	DECLARE @PLATE_NO VARCHAR(20)

	IF EXISTS (
		SELECT 1 FROM INSERTED I
		INNER JOIN VALIDATOR V
		ON 
		I.VALIDATOR_NO = V.VALIDATOR_NO
		WHERE V.ISACTIVE = 1 AND I.ISACTIVE = 1
		-- SADECE AKT›F ARA«LAR VE VALIDATORLER ›«›N KONTROL ED›L›R
		AND EXISTS (
		SELECT 1 FROM VEHICLE V2 WHERE V2.VALIDATOR_NO = I.VALIDATOR_NO
		AND V2.ISACTIVE = 1 
		AND V2.VEHICLE_ID != I.VEHICLE_ID -- KEND›S›N› HAR›« TUTTUK
		)
	)
	
	BEGIN -- 2
		-- HATALI VALIDATOR B›LG›LER›N› AL
		SELECT TOP 1
		@VALIDATOR_NO = I.VALIDATOR_NO,
		@PLATE_NO = I.PLATE FROM INSERTED I
		WHERE EXISTS (
		SELECT 1 FROM VEHICLE V2
		WHERE V2.VALIDATOR_NO = I.VALIDATOR_NO
		AND V2.ISACTIVE = 1
		AND V2.VEHICLE_ID = I.VEHICLE_ID
		)
	END --2

	-- VALIDATOR S›STEMDE KAYITLI MI
	IF EXISTS ( 
		SELECT 1 FROM INSERTED I
		WHERE NOT EXISTS (
		SELECT 1 FROM VALIDATOR V
		WHERE V.VALIDATOR_NO = I.VALIDATOR_NO
		AND V.ISACTIVE = 1
		)
	)

	BEGIN -- 3
		SELECT TOP 1 
		@VALIDATOR_NO = I.VALIDATOR_NO,
		@PLATE_NO = I.PLATE FROM INSERTED I
		WHERE NOT EXISTS (
		SELECT 1 FROM VALIDATOR V
		WHERE V.VALIDATOR_NO = I.VALIDATOR_NO
		AND V.ISACTIVE = 1
		)
	END --3

END --1

