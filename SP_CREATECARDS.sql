CREATE PROCEDURE SP_CREATECARD
@CARD_COUNT INT = 2 --  2 KART EKLENS›N
AS
BEGIN --MAIN

	SET NOCOUNT ON

	-- DE–›ﬁKENLER
	DECLARE @COUNTER INT = 0 -- SAYA«
	DECLARE @RANDOM_CARD_NO INT -- RANDOM KAR NUMARASI
	DECLARE @RANDOM_BALANCE DECIMAL(10,2) -- RANDOM BAK›YE
	DECLARE @RANDOM_CARD_TYPE_ID INT -- RANDOM KART T›P› ID
	DECLARE @CARD_TYPE VARCHAR(50) -- KART T›P›
	DECLARE @RANDOM_EXPIRATION_DATE DATETIME -- RANDOM B›T›ﬁ TAR›H›
	DECLARE @EXISTING_CARD INT -- KART EXISTS 
	DECLARE @RETRY_COUNT INT -- DENEME 
	DECLARE @SUCCESS_COUNT INT -- BAﬁARLI KAYIT SAYISI


	-- E–ER KAT AKT›F DE–›LSE AKT›F KART BULUNAMAID MESAJI G÷STER
	IF NOT EXISTS (SELECT 1 FROM CARDTYPES WHERE ISACTIVE = 1)
	BEGIN --2
		PRINT('AKT›F KART BULUNAMADI')
		RETURN
	END --2


	-- COUNTER, KART SAYISINDAN K‹«‹K OLDU–U S‹RECE «ALIﬁTIR
	WHILE @COUNTER < @CARD_COUNT
	BEGIN --3
		
		SET @RETRY_COUNT = 0
		WHILE @RETRY_COUNT < 100
		BEGIN --4
			SET @RANDOM_CARD_NO = CAST(RAND() * 90000 + 10000 AS INT)
			SELECT @EXISTING_CARD = COUNT(*) FROM CARDS WHERE CARD_NO = @RANDOM_CARD_NO
			IF @EXISTING_CARD = 0 BREAK
			SET @RETRY_COUNT = @RETRY_COUNT + 1
		END --4
		IF @RETRY_COUNT >= 100
			BEGIN --5
				PRINT('BENZERS›Z KART OLUﬁTURULMADI')
			END --5
		SELECT TOP 1 @RANDOM_CARD_TYPE_ID = CARD_TYPE_ID FROM CARDTYPES WHERE ISACTIVE = 1
		ORDER BY NEWID(); -- RASTGELE SE«ER
		SET @RANDOM_BALANCE = CAST(RAND() * 500 + 500 AS DECIMAL(10,2)) -- 500 ›LE 1000 TL ARASI E–ER +500 KOYMAZSAK 0 ›LE 500 ARASI OLMUﬁ OLUYOR
		SET @RANDOM_EXPIRATION_DATE = DATEADD(DAY,CAST(RAND()*365 * 4 + 365 AS INT) , GETDATE()) -- 365 ile 1825, 1 VE 5 YIL ARASINDA GELEB›L›R E–ER +365 OLMASAYDI 0 VE 4 YIL ARASI OLMUﬁ OLURDU.
		
		
		BEGIN --6
			INSERT INTO CARDS (CARD_NO, CARD_TYPE_ID, BALANCE, EXPIRATION_DATE, MODIFIED_DATE)
			VALUES (@RANDOM_CARD_NO, @RANDOM_CARD_TYPE_ID, @RANDOM_BALANCE, @RANDOM_EXPIRATION_DATE, GETDATE())
			SET @COUNTER = @COUNTER + 1

			SET @SUCCESS_COUNT = @SUCCESS_COUNT + 1

		END --6
	END --3


	IF @SUCCESS_COUNT > 0
	BEGIN --4
    SELECT TOP (@CARD_COUNT)
        C.CARD_ID,
        C.CARD_NO,
        CT.CARD_TYPE,
        C.BALANCE,
        C.EXPIRATION_DATE,
        C.ISACTIVE
    FROM CARDS C
    INNER JOIN CARDTYPES CT ON C.CARD_TYPE_ID = CT.CARD_TYPE_ID
    ORDER BY C.CARD_ID DESC;

	END --4

	SELECT * FROM CARDS

END --  MAIN






EXEC SP_CREATECARD 